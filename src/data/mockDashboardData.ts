// Helper function to get full case record
export function getCaseRecord(caseId: string): CaseRecordData | undefined {
  const caseFolder = MOCK_CASE_FOLDERS.find(c => c.id === caseId);
  if (!caseFolder) return undefined;

  return {
    ...caseFolder,
    claimant: caseFolder.title.split(' v. ')[0],
    employer: caseFolder.title.split(' v. ')[1]?.split(' ')[0] || 'Unknown',
    office: 'Washington, DC',
    createdAt: '2024-12-01',
    filings: MOCK_FILINGS[caseId] || [],
    parties: MOCK_PARTIES[caseId] || [
      {
        name: caseFolder.title.split(' v. ')[0],
        role: 'Claimant',
        isRepresented: false,
      },
      {
        name: caseFolder.title.split(' v. ')[1]?.split(' ')[0] || 'Unknown',
        role: 'Employer',
        isRepresented: true,
      },
    ],
    deadlines: MOCK_DEADLINES[caseId] || [],
  };
}
